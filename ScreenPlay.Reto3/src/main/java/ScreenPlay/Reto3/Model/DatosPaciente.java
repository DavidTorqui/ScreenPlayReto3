package ScreenPlay.Reto3.Model;

public class DatosPaciente {

	public String nombrepaciente;
	public String apellidopaciente;
	public String telefonopaciente;
	public String tipoDocumentopaciente;
	public String documentoIdentidadpaciente;
	
	public String getNombrepaciente() {
		return nombrepaciente;
	}
	public void setNombrepaciente(String nombrepaciente) {
		this.nombrepaciente = nombrepaciente;
	}
	public String getApellidopaciente() {
		return apellidopaciente;
	}
	public void setApellidopaciente(String apellidopaciente) {
		this.apellidopaciente = apellidopaciente;
	}
	public String getTelefonopaciente() {
		return telefonopaciente;
	}
	public void setTelefonopaciente(String telefonopaciente) {
		this.telefonopaciente = telefonopaciente;
	}
	public String getTipoDocumentopaciente() {
		return tipoDocumentopaciente;
	}
	public void setTipoDocumentopaciente(String tipoDocumentopaciente) {
		this.tipoDocumentopaciente = tipoDocumentopaciente;
	}
	public String getDocumentoIdentidadpaciente() {
		return documentoIdentidadpaciente;
	}
	public void setDocumentoIdentidadpaciente(String documentoIdentidadpaciente) {
		this.documentoIdentidadpaciente = documentoIdentidadpaciente;
	}
}